  storeInfo.addEventListener("mouseleave", function() {
    storeInfo.classList.remove("active");
  });

  document.addEventListener("DOMContentLoaded", function() {
    const slider = document.querySelector(".slider");
    const slides = document.querySelectorAll(".slide");
    const prevBtn = document.querySelector(".prev");
    const nextBtn = document.querySelector(".next");
    let counter = 0;

    // Función para avanzar al siguiente slide
    function nextSlide() {
      if (counter === slides.length - 1) {
        counter = 0;
      } else {
        counter++;
      }
      updateSlider();
    }

    // Función para retroceder al slide anterior
    function prevSlide() {
      if (counter === 0) {
        counter = slides.length - 1;
      } else {
        counter--;
      }
      updateSlider();
    }

    // Actualizar la posición del slider
    function updateSlider() {
      const slideWidth = slides[0].clientWidth;
      slider.style.transform = `translateX(${-slideWidth * counter}px)`;
    }

    // Event listeners para los botones de navegación
    nextBtn.addEventListener("click", nextSlide);
    prevBtn.addEventListener("click", prevSlide);

    // Auto play
    setInterval(nextSlide, 5000);
  });

  document.addEventListener("DOMContentLoaded", function() {
    const showMoreButton = document.getElementById("show-more-btn");
    const salesCards = document.querySelectorAll(".sales-card");
    const totalCards = salesCards.length; // Obtener el total de tarjetas

    // Ocultar todas las tarjetas que exceden las primeras 5
    for (let i = 5; i < totalCards; i++) {
      salesCards[i].classList.add("hidden");
    }

    // Agregar evento clic al botón "Mostrar más"
    showMoreButton.addEventListener("click", function() {
      // Mostrar todas las tarjetas ocultas
      salesCards.forEach(function(card) {
        card.classList.remove("hidden");
      });
      // Ocultar el botón "Mostrar más" después de mostrar todas las tarjetas
      showMoreButton.style.display = "none";
    });
  });
</script>

